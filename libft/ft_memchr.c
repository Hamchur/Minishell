#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	const char	*s;

	s = str;
	if (c > 255)
		c %= 256;
	while (n-- != 0)
	{
		if (*s == c)
			return ((void *)(s));
		s++;
	}
	return (NULL);
}
