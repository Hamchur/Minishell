#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*temp;

	i = 0;
	temp = lst;
	while (temp != NULL)
	{
		i++;
		if (temp->next == NULL)
			return (i);
		temp = temp->next;
	}
	return (i);
}
