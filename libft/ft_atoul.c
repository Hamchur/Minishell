#include "libft.h"

uint64_t	ft_atoul(const char *str)
{
	uint64_t	nbr;
	int			dgt;

	nbr = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nbr *= 10;
		dgt = *str++ - 48;
		nbr += dgt;
	}
	return (nbr);
}
